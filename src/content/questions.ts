function difficultyFor(index: number, answer: string): 1 | 2 | 3 | 4 {
  if (answer.length >= 7 || index % 20 === 0) return 4;
  if (answer.length >= 5 || index % 5 === 0) return 3;
  if (answer.length >= 4 || index % 2 === 0) return 2;
  return 1;
}

const rawQuestions = PACKS
  .filter(([category]) => category !== "职业")
  .flatMap(([category, questions]) => questions.map((question) => ({ category, question })));

export const QUESTIONS: Question[] = rawQuestions.map(({ category, question: [answer, emoji, explanation] }, index) => ({
  id: `q-${String(index + 1).padStart(3, "0")}`,
  emoji,
  answer,
  aliases: [],
  hints: [`${answer.length} 个字 · ${category}`, answer[0], `${answer[0]}${"_".repeat(Math.max(0, answer.length - 1))}`],
  category,
  difficulty: difficultyFor(index, answer),
  baseScore: 100,
  explanation,
}));

export function validateQuestions(questions: Question[]): string[] {
  const errors: string[] = [];
  if (questions.length !== 500) errors.push(`题库应为 500 题，实际为 ${questions.length}`);
  const ids = new Set<string>();
  const answers = new Set<string>();
  const clues = new Set<string>();
  for (const question of questions) {
    if (ids.has(question.id)) errors.push(`重复 ID: ${question.id}`);
    ids.add(question.id);
    if (answers.has(question.answer)) errors.push(`重复答案: ${question.answer}`);
    answers.add(question.answer);
    if (clues.has(question.emoji)) errors.push(`重复 Emoji: ${question.emoji}`);
    clues.add(question.emoji);
    if (!question.explanation.trim()) errors.push(`缺少解释: ${question.id}`);
  }
  return errors;
}
